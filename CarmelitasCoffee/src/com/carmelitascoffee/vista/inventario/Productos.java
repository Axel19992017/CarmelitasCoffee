    private void tProductosKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_tProductosKeyReleased
        if (evt.getKeyCode() == KeyEvent.VK_ENTER) {
            int fila = tProductos.getSelectedRow();
            int columna = tProductos.getSelectedColumn();
            int idProducto = Integer.parseInt(tProductos.getValueAt(fila, 0).toString());
            Producto producto = controlador.obtenerPorId(idProducto);
            String nombre = tProductos.getValueAt(fila, 1).toString();
            int cantidad = Integer.parseInt(tProductos.getValueAt(fila, 2).toString());
            double precio = Double.parseDouble(tProductos.getValueAt(fila, 3).toString());
            boolean excentoIVA = Boolean.parseBoolean(tProductos.getValueAt(fila, 4).toString());
            switch (columna) {
                // nombre
                case 1:
                    producto.setDescripcion(nombre);
                    break;
                // cantidad
                case 2:
                    producto.setCantidadEnInventario(cantidad);
                    break;
                // precio
                case 3:
                    producto.setPrecio(new BigDecimal(precio));
                    break;
                case 4:
                    producto.setExentoIva(excentoIVA);
            }
            controlador.LlenarTabla(tProductos);
        }
    }//GEN-LAST:event_tProductosKeyReleased

    private void bAgregarProductoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bAgregarProductoActionPerformed
        String descripcion = tfDescripcion.getText();
        int cantidad = Integer.parseInt(tfCantidad.getText());
        double precio = Double.parseDouble(tfPrecio.getText());
        boolean excentoIVA = cbExentoIVA.isSelected();

        Producto producto = new Producto(descripcion, new BigDecimal(precio), cantidad, excentoIVA);
        controlador.AgregarProducto(producto);
        tfDescripcion.setText("");
        tfCantidad.setText("");
        tfPrecio.setText("");
        cbExentoIVA.setSelected(false);

    }//GEN-LAST:event_bAgregarProductoActionPerformed


